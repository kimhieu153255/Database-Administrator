            RETURN '''NV'' || MANV = ''' || US || '''';
    END;
    IF ROL = 'Tai chinh' OR ROL = 'Nhan su' OR ROL = 'Ban giam doc' OR US = 'SYSTEM' THEN
        RETURN '1 = 1';
    ELSIF ROL = 'Truong phong' THEN
        BEGIN
            SELECT VALUE INTO DEP FROM SESSION_CONTEXT WHERE NAMESPACE = 'EMPLOYEE_CONTEXT' AND ATTRIBUTE = 'DEPARTMENT';
            RETURN 'PHG = ''' || DEP || '''';
        END;
    ELSIF ROL = 'Quan ly truc tiep' THEN
        RETURN '''NV'' || MANV = ''' || US || ''' OR ''NV'' || MANQL = ''' || US || '''';
    ELSIF ROL = 'Nhan vien' OR ROL = 'Truong de an' THEN
        RETURN '''NV'' || MANV = ''' || US || '''';
    ELSE
        RETURN '1 = 2';
    END IF;
END;
