BEGIN
    EXECUTE IMMEDIATE('ALTER SESSION SET "_ORACLE_SCRIPT" = TRUE');
    OPEN CUR;
    LOOP
        FETCH CUR INTO US;
        EXIT WHEN CUR%NOTFOUND;
        EXECUTE IMMEDIATE 'CREATE USER ' || US || ' IDENTIFIED BY 123';
        EXECUTE IMMEDIATE 'GRANT CREATE SESSION TO ' || US;
    END LOOP;
    CLOSE CUR;
    EXECUTE IMMEDIATE('ALTER SESSION SET "_ORACLE_SCRIPT" = FALSE');
END;
/
